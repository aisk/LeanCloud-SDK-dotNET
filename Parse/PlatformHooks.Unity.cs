    /// <summary>
    /// Wraps the custom settings object for LeanCloud so that it can be exposed as ApplicationSettings.
    /// </summary>
    private class SettingsWrapper : IDictionary<string, object> {
      private readonly IDictionary<string, object> data;

      private static SettingsWrapper wrapper;
      public static SettingsWrapper Wrapper {
        get {
          wrapper = wrapper ?? new SettingsWrapper();
          return wrapper;
        }
      }

      private SettingsWrapper() {
        var existingSettings = Load();
        if (string.IsNullOrEmpty(existingSettings)) {
          data = new Dictionary<string, object>();
          Save();
        } else {
          data = AVClient.DeserializeJsonString(existingSettings);
        }
      }

      private string Load() {
        if (settingsPath == null) {
          throw new InvalidOperationException("LeanCloud must be initialized before making any calls.");
        }
        lock (this) {
          try {
            if (IsWebPlayer) {
              return PlayerPrefs.GetString("LeanCloud.settings", null);
            } else {
              using (var fs = new FileStream(settingsPath, FileMode.Open, FileAccess.Read)) {
                var reader = new StreamReader(fs);
                return reader.ReadToEnd();
              }
            }
          } catch (Exception) {
            return null;
          }
        }
      }

      private void Save() {
        if (settingsPath == null) {
          throw new InvalidOperationException("LeanCloud must be initialized before making any calls.");
        }
        lock (this) {
          if (IsWebPlayer) {
            PlayerPrefs.SetString("LeanCloud.settings", AVClient.SerializeJsonString(data));
            PlayerPrefs.Save();
          } else {
            using (var fs = new FileStream(settingsPath, FileMode.Create, FileAccess.Write)) {
              using (var writer = new StreamWriter(fs)) {
                writer.Write(AVClient.SerializeJsonString(data));
              }
            }
          }
        }
      }

      public void Add(string key, object value) {
        data.Add(key, value);
        Save();
      }

      public bool ContainsKey(string key) {
        return data.ContainsKey(key);
      }

      public ICollection<string> Keys {
        get { return data.Keys; }
      }

      public bool Remove(string key) {
        if (data.Remove(key)) {
          Save();
          return true;
        }
        return false;
      }

      public bool TryGetValue(string key, out object value) {
        return data.TryGetValue(key, out value);
      }

      public ICollection<object> Values {
        get { return data.Values; }
      }

      public object this[string key] {
        get {
          return data[key];
        }
        set {
          data[key] = value;
          Save();
        }
      }

      public void Add(KeyValuePair<string, object> item) {
        data.Add(item);
        Save();
      }

      public void Clear() {
        data.Clear();
        Save();
      }

      public bool Contains(KeyValuePair<string, object> item) {
        return data.Contains(item);
      }

      public void CopyTo(KeyValuePair<string, object>[] array, int arrayIndex) {
        data.CopyTo(array, arrayIndex);
      }

      public int Count {
        get { return data.Count; }
      }

      public bool IsReadOnly {
        get { return data.IsReadOnly; }
      }

      public bool Remove(KeyValuePair<string, object> item) {
        if (data.Remove(item)) {
          Save();
          return true;
        }
        return false;
      }

      public IEnumerator<KeyValuePair<string, object>> GetEnumerator() {
        return data.GetEnumerator();
      }

      System.Collections.IEnumerator System.Collections.IEnumerable.GetEnumerator() {
        return data.GetEnumerator();
      }
    }

