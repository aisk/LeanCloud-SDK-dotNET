    /// <summary>
    /// Runs things inside of a Unity coroutine (some APIs require that you
    /// access them from the main thread).
    /// </summary>
    internal static void RunOnMainThread(Action action) {
      if (dispatchQueueLock.IsWriteLockHeld) {
        dispatchQueue.Enqueue(action);
        return;
      }
      dispatchQueueLock.EnterWriteLock();
      try {
        dispatchQueue.Enqueue(action);
      } finally {
        dispatchQueueLock.ExitWriteLock();
      }
    }

    /// <summary>
    /// Returns an enumerator (for use in a coroutine) that runs actions that have been dispatched.
    /// </summary>
    internal static IEnumerator RunDispatcher() {
      while (true) {
        dispatchQueueLock.EnterUpgradeableReadLock();
        try {
          // We'll only empty what's already in the dispatch queue in this iteration (so that a
          // nested dispatch behaves like nextTick()).
          int count = dispatchQueue.Count;
          if (count > 0) {
            dispatchQueueLock.EnterWriteLock();
            try {
              while (count > 0) {
                try {
                  dispatchQueue.Dequeue()();
                } catch (Exception e) {
                  // If an exception occurs, catch it and log it so that dispatches aren't broken.
                  Debug.LogException(e);
                }
                count--;
              }
            } finally {
              dispatchQueueLock.ExitWriteLock();
            }
          }
        } finally {
          dispatchQueueLock.ExitUpgradeableReadLock();
        }
        yield return null;
      }
    }

    /// <summary>
    /// Initialize the app. Called from <see cref="AVClient.Initialize(string, string)"/>. Guaranteed to be run on main thread.
    /// </summary>
    public void Initialize() {
      if (settingsPath != null) {
        return;
      }

      settingsPath = Path.Combine(Application.persistentDataPath, "LeanCloud.settings");
      // We can only set some values here since we can be sure that Initialize is always called
      // from main thread.
      isWebPlayer = Application.isWebPlayer;
      osVersion = SystemInfo.deviceModel;
      // Unity 5 has `Application.version`, but will compile-error in earlier version.
      // Reflection is the best way we can do for now.
      PropertyInfo appVersionProp = typeof(Application).GetProperty("version");
      if (appVersionProp != null) {
        appBuildVersion = (string)appVersionProp.GetValue(null, null);
      }

      // Unity 5 has `Application.bundleIdentifier`, but will compile error in earlier version.
      // Reflection is the best way we can do for now.
      PropertyInfo bundleIdProp = typeof(Application).GetProperty("bundleIdentifier");
      if (bundleIdProp != null) {
        appDisplayVersion = (string)bundleIdProp.GetValue(null, null);
      }

      // Unity 5 has `Application.productName`, but will compile error in earlier version.
      // Reflection is the best way we can do for now.
      PropertyInfo productNameProp = typeof(Application).GetProperty("productName");
      if (productNameProp != null) {
        appName = (string)productNameProp.GetValue(null, null);
      }

      settings = SettingsWrapper.Wrapper;

      if (IsAndroid) {
        try {
          CallStaticJavaUnityMethod("com.parse.AVPushUnityHelper", "registerGcm", null);
        } catch (Exception e) {
          // We don't care about the exception. If it reaches this point, it means the Plugin is misconfigured/we don't want to use
          // PushNotification. Let's just log it to developer.
          Debug.LogException(e);
        }
      }
    }

